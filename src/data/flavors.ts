export const getAllFlavors = (): string[] => {
  return FLAVOR_CATEGORIES.flatMap(c => c.items);
};

// Map items back to their parent category
export const getFlavorCategory = (flavorName: string): FlavorWheelCategory | undefined => {
  return FLAVOR_CATEGORIES.find(c => c.items.includes(flavorName));
};
